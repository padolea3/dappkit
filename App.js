const CONTRACT_ADDRESS = '0xfDba60503F321288E255B20Ac1C725e1cA830121'// Your contract address goes here

export default function App() {
  const [totalSupply, setTotalSupply] = useState(null);
  const { dapp } = useContractKit();

  const fetchTotalSupply = async () => {
    try {
      const web3 = dapp.web3;
      const contract = new web3.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS);
      const totalSupply = await contract.methods.totalSupply().call();
      setTotalSupply(totalSupply);
    } catch (error) {
      console.error(error);
    }
  };

  return (
    <DappKitProvider>
      <View style={styles.container}>
        <TouchableOpacity onPress={fetchTotalSupply}>
          <Text style={styles.button}>Fetch Total Supply</Text>
        </TouchableOpacity>
        {totalSupply && (
          <Text style={styles.text}>Total Supply: {totalSupply}</Text>
        )}
      </View>
    </DappKitProvider>
  );
}

